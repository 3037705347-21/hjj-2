function mapStageToSkill(stage: ProcessingStage): string {
  const stageSkillMap: Partial<Record<ProcessingStage, string>> = {
    'model-scanning': 'model-scanning',
    'wax-up': 'wax-up',
    'casting': 'casting',
    'porcelain': 'porcelain',
    'glazing': 'glazing',
    'finishing': 'finishing',
    'quality-check': 'quality-check',
  }
  return stageSkillMap[stage] || 'finishing'
}

